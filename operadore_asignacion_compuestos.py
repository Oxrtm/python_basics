print('*** Operadores de Asignacion compuestos ***')
a, b = 10, 15
print(f'Valor inicial a: {a}')
print(f'Valor inicial b: {b}')

# Operador compuesto de +=
a += b # a = a + b
print(f'Resultado operador a += b es: {a}')

# Operador compuesto de -=
a = 10
a -= b # a = a - b
print(f'Resultado operador a -= b es: {a}')

# Operador compuesto de *=
a = 10
a *= b # a = a * b
print(f'Resultado operador a *= b es : {a}')

# Operador compuesto de /=
a = 10
a /= b # a = a / b
print(f'Resultado operador a /= b es: {a}')

